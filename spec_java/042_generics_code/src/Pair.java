public class Pair<String> {
    //attributi stringa s1 e s2
    String s1;
    String s2;
    //costruttore
    public Pair(String s1, String s2) {
        this.s1 = s1;
        this.s2 = s2;
    }
    // metodi
    public String getS1() { return s1; }
    public String getS2() { return s2; }
    public void setS1(String _s1) { this.s1 = _s1; }
    public void setS2(String _s2) { this.s2 = _s2; }

}
